import java.util.Scanner;
class Solution {

    public static void main(String[] args) {
	   
        Scanner scan = new Scanner(System.in);
        int n = scan.nextInt();
        int []a=new int[n];
for(int i=0;i<n;i++)
{
    a[i]=scan.nextInt();
}

for(int k=0;k<a.length;k++)
{
    System.out.println(a[k]);
}
	}
}



        

       